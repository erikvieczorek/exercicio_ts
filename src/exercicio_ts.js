"use strict";
// Função de multiplicação
function multiplicar(a, b) {
    return a * b;
}
// Função de saudação
function saudacao(nome) {
    return "Olá " + nome + "!";
}
// Exemplos de uso
const resultadoMultiplicacao = multiplicar(1.772455923288362, 1.772455923288362);
const mensagemSaudacao = saudacao("Ebac");
console.log(mensagemSaudacao);
console.log("O resultado da multiplicação é:", resultadoMultiplicacao);
